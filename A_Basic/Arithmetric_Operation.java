package A_Basic;

public class Arithmetric_Operation {
    public static void main(String[] args) {
        int x = 5, y = 2;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);

        //Double Datatype
        double a = 3.1415;
        System.out.println(a);
        float b = 3.14f;
        System.out.println(b);

        //AO for double
        double c = 5, d = 2;
        System.out.println(c / d);
    }
}
