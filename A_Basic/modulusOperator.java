package A_Basic;
public class modulusOperator {
    public static void main(String[] args) {
        int a = 8, b = 3;
        System.out.println(a%b);
        System.out.println(37%10);
        System.out.println(-37%10);
        System.out.println(37%-10);
        System.out.println(-37%-10);
    }
}
