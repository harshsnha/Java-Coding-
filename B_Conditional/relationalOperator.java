package B_Conditional;

public class relationalOperator {
    public static void main(String[] args) {
        int a =5, b = 5, c=4;
        System.out.println(a==b);
        System.out.println(a != c);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
